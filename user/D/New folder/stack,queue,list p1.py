#1.stack
stack=["parth","sahil","kartik","shubhu"]
print(stack)

#Stack adding element using append method
stack.append("prajesh")
print("After adding element in stack:",stack)
stack.append("nikil")
print("After adding element in stack:",stack)


#stack removing element using pop method
stack.pop()
print("after removing element in stack:",stack)

